class Solution
{
public:
//tc=o(n) as each node is visited once
//sc=o(H) where H is height of tree
    void solve(Node *root,int sum,int &maxsum,int l,int &maxl)
    {
        //base case
        if(root==NULL)
        {
            //means path ended
            if(l>maxl)
            {
                maxl=l;
                maxsum=sum; //coz maxl is greater
            }
            else if(l==maxl)
            {
                //is l is equal to maxl then no need to update just take max of l and maxl
                maxsum=max(sum,maxsum);
            }
            return ;
        } 
        sum=sum+root->data; //updating sum by adding the respectove nodes data
        //for left child
        solve(root->left,sum,maxsum,l+1,maxl);
        //for right child
        solve(root->right,sum,maxsum,l+1,maxl);
        
        
    }
    int sumOfLongRootToLeafPath(Node *root)
    {
        //code here
        int l=0;
        int maxl=0;
        int sum=0;
        int maxsum=INT_MIN;
        solve(root,sum,maxsum,l,maxl);
        return maxsum;
    }
};

